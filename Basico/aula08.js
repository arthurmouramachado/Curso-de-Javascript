let n1 = 10;
let n2 = 11;

let res = n1 & n2; // 10 & 11 = 10
console.log(res); // 10

res = n1 | n2; // 10 | 11 = 11
console.log(res); // 11

res = n1 ^ n2; // 10 ^ 11 = 1
console.log(res); // 1

res = n1 << 1; // 10 << 1 = 20
console.log(res); // 20

res = n1 >> 1; // 10 >> 1 = 5
console.log(res); // 5

